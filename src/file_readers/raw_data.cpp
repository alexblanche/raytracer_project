/* Creates a file file_name, and writes in it the resolution of the image, the number of rays used to generate it,
   and the raw data of the matrix
   Returns true if the operation was successful */
/* Format:

   width:1366 height:768 number_of_rays:10
   2550.0000000000 2000.0000000000 1367.4666666666
   ...
   
   width * height of 3 doubles (red, green, blue) separated by spaces,
   pixel (i,j) at line j*width + i
   line k representing pixel (k % width, k / width)
*/
exit_status export_raw(const char* file_name, const unsigned int number_of_rays, const std::vector<std::vector<rt::color>>& matrix) {

    FILE* file = fopen(file_name, "w");

    if (file == nullptr) {
        printf("Error, file %s could not be created\n", file_name);
        return exit_status::Failure;
    }

    try {
    
        const unsigned int width = matrix.size();
        const unsigned int height = matrix[0].size();

        const int ret0 = fprintf(file, "width:%u height:%u number_of_rays:%u\n",
            (unsigned int) width, (unsigned int) height, number_of_rays);

        if (ret0 < 0) {
            printf("Writing error at first line of %s\n", file_name);
            throw std::runtime_error("");
        }

        for(unsigned int j = 0; j < height; j++) {
            for(unsigned int i = 0; i < width; i++) {
                const rt::color& c = matrix[i][j];
                const int ret = fprintf(file, "%lf %lf %lf\n", c.get_red(), c.get_green(), c.get_blue());
                if (ret < 0) {
                    printf("Writing error at color line %u of %s\n", width*j + i, file_name);
                    throw std::runtime_error("");
                }
            }
        }

        fclose(file);
        return exit_status::Success;

    }
    catch(const std::exception& e) {
        fclose(file);
        return exit_status::Failure;
    }
}

/* Reads a file file_name generated by export_raw, and returns a matrix with its content
   Writes the number of rays in the associated variable */
std::optional<std::vector<std::vector<rt::color>>> read_raw(const char* file_name, unsigned int& number_of_rays) {

    FILE* file = fopen(file_name, "r");

    if (file == nullptr) {
        printf("Error, file %s not found\n", file_name);
        return std::nullopt;
    }

    try {

        unsigned int width, height;
        const int ret0 = fscanf(file, "width:%u height:%u number_of_rays:%u\n",
            &width, &height, &number_of_rays);

        if (ret0 < 0) {
            printf("Reading error at first line of file %s\n", file_name);
            throw std::runtime_error("");
        }

        std::vector<std::vector<rt::color>> matrix(width, std::vector<rt::color>(height));

        for(unsigned int j = 0; j < height; j++) {
            for(unsigned int i = 0; i < width; i++) {
                double r, g, b;
                const int ret = fscanf(file, "%lf %lf %lf\n", &r, &g, &b);
                if (ret < 0) {
                    printf("Reading error at color line %u of file %s\n", width*j + i, file_name);
                    throw std::runtime_error("");
                }
                matrix[i][j] = rt::color(r, g, b);
            }
        }

        fclose(file);
        return matrix;

    }
    catch(const std::exception& e) {
        fclose(file);
        return std::nullopt;
    }
}

/* Combines the n files whose names are in the array source_file_names into one bmp file dest_bmp_name (extension .bmp)
   and one raw data file dest_raw_name (extension .rtdata)
   Returns true if the operation was successful */
exit_status combine_raw(const std::string& dest_bmp_name, const std::string& dest_raw_name,
    const std::span<const std::string> source_file_names, const float gamma) {

    printf("Merging %lu files...\n", source_file_names.size());

    FILE* file0 = fopen(source_file_names[0].c_str(), "r");

    if (file0 == nullptr) {
        printf("Error: first file %s not found\n", source_file_names[0].c_str());
        return exit_status::Failure;
    }

    /* Determination of the size of the matrix */
    unsigned int width, height;
    const int ret0 = fscanf(file0, "width:%u height:%u",
        &width, &height);

    if (ret0 < 0) {
        printf("Reading error at first line of file %s\n", source_file_names[0].c_str());
        fclose(file0);
        return exit_status::Failure;
    }

    std::vector<std::vector<rt::color>> matrix(width, std::vector<rt::color>(height));

    unsigned int total_number_of_rays = 0;

    /* Adding the value of each file to matrix */
    for (const std::string& name : source_file_names) {

        FILE* file = fopen(name.c_str(), "r");
        if (file == NULL) {
            printf("Error: file %s not found\n", name.c_str());
            return exit_status::Failure;
        }

        unsigned int width_k, height_k, number_of_rays_k;
        const int retk0 = fscanf(file, "width:%u height:%u number_of_rays:%u\n",
            &width_k, &height_k, &number_of_rays_k);

        if (retk0 < 0) {
            printf("Reading error at first line of file %s\n", name.c_str());
            fclose(file);
            return exit_status::Failure;
        }

        if (width_k != width || height_k != height) {
            printf("Error, incorrect width or height in file %s\n", name.c_str());
            fclose(file);
            return exit_status::Failure;
        }

        total_number_of_rays += number_of_rays_k;

        for(unsigned int j = 0; j < height; j++) {
            const unsigned int widthj = width * j;
            for(unsigned int i = 0; i < width; i++) {
                double r, g, b;
                const int ret = fscanf(file, "%lf %lf %lf\n", &r, &g, &b);
                if (ret < 0) {
                    printf("Reading error at color line %u of file %s\n", widthj + i, name.c_str());
                    fclose(file);
                    return exit_status::Failure;
                }
                matrix[i][j] += rt::color(r, g, b);
            }
        }
        fclose(file);
    }

    /* Exporting the matrix as a bmp file */
    const exit_status success_bmp = write_bmp(dest_bmp_name.c_str(), matrix, total_number_of_rays, gamma);
    const exit_status success_raw = export_raw(dest_raw_name.c_str(), total_number_of_rays, matrix);

    if ((success_bmp && success_raw) == exit_status::Success)
        return exit_status::Success;
    
    if (success_bmp == exit_status::Failure)
        printf("Error in generating the bmp file %s\n", dest_bmp_name.c_str());
    if (success_raw == exit_status::Failure)
        printf("Error in generating the raw data file %s\n", dest_raw_name.c_str());

    return exit_status::Failure;
}